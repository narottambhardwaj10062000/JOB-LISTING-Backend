//IMPORTS
const mongoose = require("mongoose");

//Creating User Schema
const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    mobile: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    }
})

//EXPORTING THE MODEL
module.exports = mongoose.model("User", userSchema);
